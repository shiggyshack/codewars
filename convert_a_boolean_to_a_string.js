// Note: Only valid inputs will be given.


function booleanToString(b){
    //Checks first to see if the boolean value is 1, if so instructions are executed
    if(b)
      {
        return "true";
      }
    //Otherwise, these instructions are executed
    else {
      return "false";
    }
  }